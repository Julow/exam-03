#include <unistd.h>

static void		put_char(char c)
{
	if (c >= 'a' && c <= 'z')
		c -= 32;
	else if (c >= 'A' && c <= 'Z')
		c += 32;
	write(1, &c, 1);
}

int				main(int argc, char **argv)
{
	int			i;

	if (argc == 2)
	{
		i = -1;
		while (argv[1][++i] != '\0')
			put_char(argv[1][i]);
	}
	write(1, "\n", 1);
	return (0);
}
